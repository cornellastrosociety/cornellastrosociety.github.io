<h3> Planets<br><br>
</b>Throughout the course of the year, all of the major Planets can be viewed from the Fuertes Observatory. 
<br><br>
Currently, Jupiter, appearing in the constellation Ophiuchus, dominates the southwestern sky during the early evening hours. At -2.2 Magnitude, it is easily visible to the naked eye,  appearing brighter than any stars. When view through the telescope, Jupiter's four largest 'moons', can be seen orbiting the planet on any given night.  
<br><br>
To the persistent and skilled observer, Mercury is visible through binoculars very low in the western sky immediately following sunset.  
<br><br>
Neptune and Uranus can be viewed later in the evening, but at magnitudes 7.8 and 5.7, respectively, they are only visible through the telescope. 
<br><br>
Mars rises around midnight, shining brightly orange (at magnitude +0.3) high in the predawn sky. It will make its way into our night sky late this fall in the constellation of Taurus, and will be well placed for excellent evening viewing by winter.  
<br><br>
Saturn is not long behind it, at the moment just visible at +0.6 magnitude near dawn in the constellation Leo, below the brilliant "morning star" Venus, which, at -4.4 magnitude, is at its brightest for the year.  While Saturn rises earlier each night, eventually making its way into our evening skies by spring, Venus will remain shining brilliantly in the morning sky for the next six months.<b></h3>

<h3>Star Clusters</h3>
<!-- Year-round -->
<p class="desc">Star clusters are a common Fuertes target throughout the year. Clusters come in two 
types. <span class="defn">Open clusters</span> are groups of newly formed stars. 
They are typically within a few thousand light years of the Earth and appear through the telescope 
as a dense collection of a few dozen to a few hundred stars. What you are actually seeing are 
only the brightest cluster members; most of the stars are too faint to see except in photographs.
</p><p class="desc">
<!-- Early April - Late November -->
<span class="defn">Globular clusters</span> are groups of up to a million stars 
that (with a few exceptions) date from the formation of the Galaxy. They tend to be 10-50,000 light 
years away and appear through the telescope as fuzzy balls. You can make out individual stars only 
on the outskirts; farther in there are far too many to tell apart. The night sky of a planet in 
one of these clusters would have thousands of bright stars (some brighter than our Moon), and a 
sky that never grows darker than our twilights.</p>
<h3>Planetary Nebulae</h3>
<!-- Early June - Mid October -->

<p class="desc">When fairly small stars, such as the Sun, end their lives, their outer layers detach
to form an expanding shell of gas called a <span class="defn">planetary nebula</span>. Planetary nebulae 
typically last a hundred thousand years (much longer than recorded history, but almost nothing on 
cosmological timescales) or less before they fade into interstellar space. The Ring Nebula is 
the nicest example of a planetary nebula, and one we will be observing very often during the 
summer and early fall. Though it's too faint to see through the Fuertes telescope, the center of the 
nebula contains a white dwarf - the remains of the star's core, still glowing with left-over 
heat.</p>

<h3>Galaxies</h3>
<!-- Year Round -->

<p class="desc">While galaxies come in a range of shapes and sizes, the ones that you can 
easily spot through the Fuertes telescope are all fairly large (the size of the Milky Way 
or larger) and appear to the eye as elliptical blobs. Some galaxies do have spiral arms, of 
course, but those are usually hard to see even with a telescope much larger than ours. 
Arms and other subtle features usually show up only in long exposure photographs.</p>
<!-- Mid January - Late June -->
